/* 5. Take two int values from user and print greatest among them.*/


public class Question5 {

    public static void main(String[] args) {
        int num1 = 42;
        int num2 = 63;

        int greatest;

        if (num1 > num2) {
            greatest = num1;
        } else {
            greatest = num2;
        }

        System.out.println("The greatest value is: " + greatest);
    }
}













